alert("Direi se o valor informado é positivo, negativo ou zero.");

var valor=parseInt(prompt("Digite algum valor:"));

if (valor >= 1 ){
    alert("O valor " + valor + " é POSITIVO.");
}

if (valor == 0 ){
    alert("O valor " + valor + " é ZERO.");
}

if (valor <= -1 ){
    alert("O valor " + valor + " é NEGATIVO.");
}

